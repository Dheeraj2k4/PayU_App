/** Returns the correct icon for a subscription label or falls back based on category */
export function getServiceIcon(label: string, color: string, size = 22): React.ReactElement {
  const l = label.toLowerCase();
  if (l.includes('spotify')) return <SpotifyIcon size={size} />;
  if (l.includes('netflix')) return <NetflixIcon size={size} />;
  if (l.includes('youtube')) return <YoutubeIcon size={size} />;
  if (l.includes('google')) return <GoogleIcon size={size} />;
  if (l.includes('bill')) return <BillsIcon size={size} color={color} />;
  return <OtherIcon size={size} color={color} />;
}
